export function generateId() {
  const s = [];
  const hexDigits = '0123456789';
  for (let i = 0; i < 16; i++) {
      s[i] = hexDigits.substr(Math.floor(Math.random()*10), 1)
  }
  const uuid = s.join('');
  return uuid;
}
